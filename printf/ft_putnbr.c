#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	i;

	if (!n)
		return (0);
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	i = 0;
	if (n < 0)
	{
		n *= -1;
		write(1, "-", 1);
		i++;
	}
	i += ft_putunsigned(n);
	return (i);
}
